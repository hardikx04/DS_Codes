import java.util.PriorityQueue;

class PriorityQueueEx{
    public static void main(String[] args) {

        
        PriorityQueue<Integer> numbers = new PriorityQueue<>();

        numbers.add(2);
        numbers.add(4);
        numbers.add(0);
        numbers.add(9);
        numbers.add(6);
        System.out.println("PriorityQueue: " + numbers);

      
    }
}